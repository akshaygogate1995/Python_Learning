# Arithmetic Operators

a = 3
b = 21
c = a + b
print(c)

# Assignment Operators

a = 4-2   # Assign 4-2 to a 
print(a)
b = 3
b += 4    # Increment the value of b by 4 and then assign it to b 
print(b)
c = 9
c -=2
print(c)  # Decrement the value of c by 2 and then assign it to c

d = 2
d *= 3
print(d)  # Multiply the value of d by 3 and then assign it to d

e = 20
e /= 4
print(e)  # Divide the value of e by 4 and then assign it to e


# Comparison Operators

d = 5<4
print(d)

e = 5>4
print(e)

f = 5!=8
print(f)

g = 5!=5
print(g)

h = 5==5
print(h)


# Logical Operators

w = True or False

# Truth table for 'or'
print("True or False is ", True or False)
print("True or True is", True or True)
print("False or True is", False or True)
print("False or False is", False or False)

# Truth table for 'and'
print("True and False is ", True and False)
print("True and True is", True and True)
print("False and True is", False and True)
print("False and False is", False and False)

print(not(True))
print(not(False))